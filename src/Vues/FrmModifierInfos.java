    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        dcDateNaissance.setDateFormatString("dd/MM/yyyy");
        ctrlUser = new CtrlUser();
        this.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
        sdf = new SimpleDateFormat("yyyy-MM-dd");
        
        txtNom.setText(unUser.getNom());
        txtPrenom.setText(unUser.getPrenom());
        txtCodePostal.setText(unUser.getCodePostal());
        txtTelephone.setText(unUser.getTelephone());
        txtAdresse.setText(unUser.getAdresse());
        txtVille.setText(unUser.getVille());
        try {
            if (unUser.getDateDeNaissance() != "") {
                dcDateNaissance.setDate(sdf.parse(unUser.getDateDeNaissance()));
            }
        } catch (ParseException ex) {
            Logger.getLogger(FrmModifierInfos.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (unUser.getSexe() == 0) {
            rbHomme.setSelected(true);
        }
        else {
            rbFemme.setSelected(true);
        }
        
        
        ImageIcon icon = new ImageIcon(getClass().getResource("/Images/enregistrer.png"));
        Image img = icon.getImage();
        img = img.getScaledInstance(lblLogoEnregistrer.getWidth(), lblLogoEnregistrer.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon ic = new ImageIcon(img);
        lblLogoEnregistrer.setIcon(ic);
        
        icon = new ImageIcon(getClass().getResource("/Images/retour.png"));
        img = icon.getImage();
        img = img.getScaledInstance(lblLogoRetour.getWidth(), lblLogoRetour.getHeight(), Image.SCALE_SMOOTH);
        ic = new ImageIcon(img);
        lblLogoRetour.setIcon(ic);
    }//GEN-LAST:event_formWindowOpened

