    private void enregistrerModif() {
        if (txtNom.getText().equals("")) {
            txtNom.requestFocusInWindow();
            JOptionPane.showMessageDialog(this, "Veuillez saisir votre nom", "Erreur", JOptionPane.WARNING_MESSAGE);
        }
        else if (txtPrenom.getText().equals("")){
            txtPrenom.requestFocusInWindow();
            JOptionPane.showMessageDialog(this, "Veuillez saisir votre prénom", "Erreur", JOptionPane.WARNING_MESSAGE);
        }
        else if (txtTelephone.getText().equals("")){
            txtTelephone.requestFocusInWindow();
            JOptionPane.showMessageDialog(this, "Veuillez saisir votre numéro de téléphone", "Erreur", JOptionPane.WARNING_MESSAGE);
        }
        else {
            unUser.setNom(txtNom.getText());
            unUser.setPrenom(txtPrenom.getText());
            int sexe = 0;
            if (rbFemme.isSelected()) {
                sexe = 1;
            }
            unUser.setSexe(sexe);
            unUser.setDateDeNaissance(null);
            if (dcDateNaissance.getDate() != null) {
               unUser.setDateDeNaissance(sdf.format(dcDateNaissance.getDate())); 
            }
            unUser.setAdresse(txtAdresse.getText());
            unUser.setCodePostal(txtCodePostal.getText());
            unUser.setVille(txtVille.getText());
            unUser.setTelephone(txtTelephone.getText());

            ctrlUser.MaJInfos(unUser);

            JOptionPane.showMessageDialog(this, "Votre profil a bien été mis à jour", "Enregistré", JOptionPane.INFORMATION_MESSAGE);
            retour();
        }
    }
    
    
    private void retour() {
        if (unUser.getStatut() == 2) {
            FrmInterfaceResponsable frm = new FrmInterfaceResponsable(unUser);
            this.setVisible(false);
            frm.setVisible(true);
        }
        else {
            FrmInterfacePrincipale frm = new FrmInterfacePrincipale(unUser);
            this.setVisible(false);
            frm.setVisible(true);
        }
    }
    
