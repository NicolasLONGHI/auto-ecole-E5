    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        this.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
        lblTitre.setText("Bienvenue " + unUser.getPrenom());
        ctrlPlanning = new CtrlPlanning();
        
        if (unUser.getStatut() == 0) {
            btnLeconLicence.setText("Reserver une le√ßon");
            mdl = new ModelJTable();
            mdl.LoadDataPlanning(ctrlPlanning.GetPlanningByEleveId(unUser.getCodeUser()));
            tblPlanning.setModel(mdl);
        }
        else {
            btnLeconLicence.setText("Ajouter une licence");
            mdl = new ModelJTable();
            mdl.LoadDataPlanning(ctrlPlanning.GetPlanningByMoniteurId(unUser.getCodeUser()));
            tblPlanning.setModel(mdl);
        }
        
        
        ImageIcon icon = new ImageIcon(getClass().getResource("/Images/deconnexion.png"));
        Image img = icon.getImage();
        img = img.getScaledInstance(lblLogoDeconnexion.getWidth(), lblLogoDeconnexion.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon ic = new ImageIcon(img);
        lblLogoDeconnexion.setIcon(ic);
    }//GEN-LAST:event_formWindowOpened

